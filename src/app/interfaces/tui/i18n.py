def t(key: str, locale: str = DEFAULT_LOCALE, **kwargs: Any) -> str:
    """Return localized string for *key*."""
    loc = locale if locale in MESSAGES else DEFAULT_LOCALE
    text = MESSAGES[loc].get(key) or MESSAGES[DEFAULT_LOCALE].get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text


def step_labels(locale: str = DEFAULT_LOCALE) -> list[str]:
    """Return localized stepper labels."""
    return [t(f"step.{name}", locale) for name in _STEP_KEYS]


def get_locale_from_app(app: Any) -> str:
    """Read ui_locale from app.wizard_state."""
    try:
        loc = app.wizard_state.ui_locale  # type: ignore[attr-defined]
        return loc if loc in LOCALES else DEFAULT_LOCALE
    except AttributeError:
        return DEFAULT_LOCALE
